// Apply common translations to locale files
for (const [locale, trans] of Object.entries(translations)) {
	const fp = path.join(messagesDir, locale + '.json');
	if (!fs.existsSync(fp)) continue;
	const data = JSON.parse(fs.readFileSync(fp, 'utf8'));
	data.tools_common = trans.common;
	// Tool-specific keys stay in English for now (they're mostly proper nouns and short labels)
	if (!data.tools) data.tools = {};
	fs.writeFileSync(fp, JSON.stringify(data, null, '\t') + '\n', 'utf8');
	console.log('✓ ' + locale + ' common keys added');
}

// For locales without explicit translations, copy English common keys
const allLocales = ['ba', 'el', 'hr', 'id', 'it', 'ko', 'pt-BR', 'tr', 'zh-Hans', 'zh-Hant'];
for (const locale of allLocales) {
	const fp = path.join(messagesDir, locale + '.json');
	if (!fs.existsSync(fp)) continue;
	const data = JSON.parse(fs.readFileSync(fp, 'utf8'));
	if (!data.tools_common) data.tools_common = common; // English fallback
	if (!data.tools) data.tools = {};
	fs.writeFileSync(fp, JSON.stringify(data, null, '\t') + '\n', 'utf8');
	console.log('✓ ' + locale + ' (English fallback for common)');
}

